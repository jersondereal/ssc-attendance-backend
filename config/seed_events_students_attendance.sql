-- =============================================================================
-- 3. Attendance: every student (24-xxxx) × every event, random status
-- =============================================================================
INSERT INTO attendance (student_id, event_id, status, is_paid)
SELECT s.student_id,
    e.id,
    CASE (floor(random() * 3))::int
        WHEN 0 THEN 'Present'
        WHEN 1 THEN 'Absent'
        ELSE 'Excused'
    END,
    random() < 0.4
FROM students s
CROSS JOIN events e
WHERE s.student_id LIKE '24-%'
ON CONFLICT (student_id, event_id) DO NOTHING;
